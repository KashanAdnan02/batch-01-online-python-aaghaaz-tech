# ### **Problem 2**
# You are given two tuples, each containing two integers. Write a program that calculates the dot product of these two tuples. The dot product is the sum of the products of the corresponding elements.

# **Example:**  
# - **Input:** `(1, 2)` and `(3, 4)`  
# - **Output:** `11`  
#   - (Because 1*3 + 2*4 = 3 + 8 = 11)

